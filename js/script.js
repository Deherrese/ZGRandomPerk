    function precargarImagenesDelRol(role, callback) {

        const boton = document.getElementById('btn-generar-click');

        boton.classList.add('disabled');

        boton.innerHTML = "<span>Cargando Perks...</span>";



        let descargasOcurriendo = 0;

        const total = DB[role].length;



        DB[role].forEach(p => {

            const ruta = `img/perks/${p.img}`;

            if (imagenesCargadas[ruta]) {

                descargasOcurriendo++;

                if(descargasOcurriendo === total) callback();

                return;

            }

            const imgObjeto = new Image();

            imgObjeto.src = ruta;

            imgObjeto.onload = imgObjeto.onerror = () => {

                imagenesCargadas[ruta] = true;

                descargasOcurriendo++;

                if(descargasOcurriendo === total) callback();

            };

        });

    }



